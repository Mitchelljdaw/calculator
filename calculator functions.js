function add(a, b) {
    if (a != null && b != null)
    return a + b;
}

function subtract(a, b) {
    if (a != null && b != null)
    return a - b;
}

function multiply(a, b){
    if (a != null && b != null)
    return a * b;
}

function divide(a, b){
    if (a != null && b != null)
    return a / b;
}